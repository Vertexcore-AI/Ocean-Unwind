'use client';
import { useEffect, useRef, useState } from 'react';
import { Canvas, extend, useFrame } from '@react-three/fiber';
import { useGLTF, useTexture, Environment, Lightformer } from '@react-three/drei';
import {
  BallCollider,
  CuboidCollider,
  Physics,
  RigidBody,
  useRopeJoint,
  useSphericalJoint,
  RigidBodyProps
} from '@react-three/rapier';
import { MeshLineGeometry, MeshLineMaterial } from 'meshline';
import * as THREE from 'three';

extend({ MeshLineGeometry, MeshLineMaterial });

interface LanyardProps {
  cardImage: string;
  position?: [number, number, number];
  gravity?: [number, number, number];
  fov?: number;
  transparent?: boolean;
}

export default function Lanyard({
  cardImage,
  position = [0, 0, 20],
  gravity = [0, -40, 0],
  fov = 30,
  transparent = true
}: LanyardProps) {
  const [isMobile, setIsMobile] = useState<boolean>(() => typeof window !== 'undefined' && window.innerWidth < 768);

  useEffect(() => {
    const handleResize = (): void => setIsMobile(window.innerWidth < 768);
    window.addEventListener('resize', handleResize);
    return () => window.removeEventListener('resize', handleResize);
  }, []);

  return (
    <div className="relative w-full h-[500px] flex justify-center items-center" style={{ touchAction: 'none', zIndex: 10 }} data-lenis-prevent-wheel data-lenis-prevent-touch>
      <Canvas
        camera={{ position, fov }}
        dpr={[1, isMobile ? 1.5 : 2]}
        gl={{ alpha: transparent }}
        onCreated={({ gl }) => gl.setClearColor(new THREE.Color(0x414987), transparent ? 0 : 1)}
        style={{ width: '100%', height: '100%', cursor: 'grab' }}
      >
        <ambientLight intensity={Math.PI} />
        <Physics gravity={gravity} timeStep={isMobile ? 1 / 30 : 1 / 60}>
          <Band isMobile={isMobile} cardImage={cardImage} />
        </Physics>
        <Environment blur={0.75}>
          <Lightformer
            intensity={2}
            color="white"
            position={[0, -1.5, 5]}
            rotation={[0, 0, Math.PI / 3]}
            scale={[100, 0.1, 1]}
          />
          <Lightformer
            intensity={3}
            color="white"
            position={[-1, -1, 1]}
            rotation={[0, 0, Math.PI / 3]}
            scale={[100, 0.1, 1]}
          />
          <Lightformer
            intensity={3}
            color="white"
            position={[1, 1, 1]}
            rotation={[0, 0, Math.PI / 3]}
            scale={[100, 0.1, 1]}
          />
          <Lightformer
            intensity={10}
            color="white"
            position={[-10, 0, 14]}
            rotation={[0, Math.PI / 2, Math.PI / 3]}
            scale={[100, 10, 1]}
          />
        </Environment>
      </Canvas>
    </div>
  );
}

interface BandProps {
  maxSpeed?: number;
  minSpeed?: number;
  isMobile?: boolean;
  cardImage: string;
}

function Band({ maxSpeed = 50, minSpeed = 0, isMobile = false, cardImage }: BandProps) {
  const band = useRef<any>(null);
  const fixed = useRef<any>(null);
  const j1 = useRef<any>(null);
  const j2 = useRef<any>(null);
  const j3 = useRef<any>(null);
  const card = useRef<any>(null);

  const vec = new THREE.Vector3();
  const ang = new THREE.Vector3();
  const rot = new THREE.Vector3();
  const dir = new THREE.Vector3();

  const segmentProps: any = {
    type: 'dynamic' as RigidBodyProps['type'],
    canSleep: true,
    colliders: false,
    angularDamping: 4,
    linearDamping: 4
  };

  const { nodes, materials } = useGLTF('/card.glb') as any;
  const lanyardTexture = useTexture('/lanyard.png');
  const cardTexture = useTexture(cardImage);

  // Adjust texture to show full image (zoom out)
  cardTexture.repeat.set(1.7, 1.4);
  cardTexture.center.set(0.0, 0.2);
  const [curve] = useState(
    () =>
      new THREE.CatmullRomCurve3([new THREE.Vector3(), new THREE.Vector3(), new THREE.Vector3(), new THREE.Vector3()])
  );
  const [dragged, drag] = useState<false | THREE.Vector3>(false);
  const [hovered, hover] = useState(false);

    useRopeJoint(fixed, j1, [[0, 0, 0], [0, 0, 0], 1.5]);
  useRopeJoint(j1, j2, [[0, 0, 0], [0, 0, 0], 1.5]);
  useRopeJoint(j2, j3, [[0, 0, 0], [0, 0, 0], 0.5]);
  useSphericalJoint(j3, card, [
    [0, 0., 0.0],
    [0.0, 1.3, 0]
  ]);

  

  useEffect(() => {
    if (hovered) {
      document.body.style.cursor = dragged ? 'grabbing' : 'grab';
      return () => {
        document.body.style.cursor = 'auto';
      };
    }
  }, [hovered, dragged]);

  useFrame((state, delta) => {
    if (dragged && typeof dragged !== 'boolean') {
      vec.set(state.pointer.x, state.pointer.y, 0.5).unproject(state.camera);
      dir.copy(vec).sub(state.camera.position).normalize();
      vec.add(dir.multiplyScalar(state.camera.position.length()));
      [card, j1, j2, j3, fixed].forEach(ref => ref.current?.wakeUp());
      card.current?.setNextKinematicTranslation({
        x: vec.x - dragged.x,
        y: vec.y - dragged.y,
        z: vec.z - dragged.z
      });
    }
    if (fixed.current) {
      [j1, j2].forEach(ref => {
        if (!ref.current.lerped) ref.current.lerped = new THREE.Vector3().copy(ref.current.translation());
        const clampedDistance = Math.max(0.1, Math.min(1, ref.current.lerped.distanceTo(ref.current.translation())));
        ref.current.lerped.lerp(
          ref.current.translation(),
          delta * (minSpeed + clampedDistance * (maxSpeed - minSpeed))
        );
      });
      curve.points[0].copy(j3.current.translation());
      curve.points[1].copy(j2.current.lerped);
      curve.points[2].copy(j1.current.lerped);
      curve.points[3].copy(fixed.current.translation());
      band.current.geometry.setPoints(curve.getPoints(isMobile ? 16 : 32));
      ang.copy(card.current.angvel());
      rot.copy(card.current.rotation());
      card.current.setAngvel({ x: ang.x, y: ang.y - rot.y * 0.25, z: ang.z });
    }
  });

  curve.curveType = 'chordal';
  lanyardTexture.wrapS = lanyardTexture.wrapT = THREE.RepeatWrapping;

  return (
    <>
      <group position={[0, 4, 0]}>
        <RigidBody ref={fixed} {...segmentProps} type={'fixed' as RigidBodyProps['type']} />
        <RigidBody position={[0.4, 0, 0]} ref={j1} {...segmentProps} type={'dynamic' as RigidBodyProps['type']}>
          <BallCollider args={[0.1]} />
        </RigidBody>
        <RigidBody position={[1.5, 0, 0]} ref={j2} {...segmentProps} type={'dynamic' as RigidBodyProps['type']}>
          <BallCollider args={[0.1]} />
        </RigidBody>
        <RigidBody position={[1.5, 0, 0]} ref={j3} {...segmentProps} type={'dynamic' as RigidBodyProps['type']}>
          <BallCollider args={[0.1]} />
        </RigidBody>
        <RigidBody
          position={[2, 0, 0]}
          ref={card}
          {...segmentProps}
          type={dragged ? ('kinematicPosition' as RigidBodyProps['type']) : ('dynamic' as RigidBodyProps['type'])}
        >
          <CuboidCollider args={[1.2, 1.6875, 0.015]} />
          <group
            scale={4.8}
            position={[0, -0.1, -0.5]}
            rotation={[0, 0, Math.PI]}
          >
            <mesh
              geometry={nodes.card.geometry}
              onPointerOver={() => hover(true)}
              onPointerOut={() => hover(false)}
              onPointerUp={(e: any) => {
                e.target.releasePointerCapture(e.pointerId);
                drag(false);
              }}
              onPointerDown={(e: any) => {
                e.target.setPointerCapture(e.pointerId);
                drag(new THREE.Vector3().copy(e.point).sub(vec.copy(card.current.translation())));
              }}
            >
              <meshPhysicalMaterial
                map={cardTexture}
                map-anisotropy={16}
                clearcoat={isMobile ? 0 : 1}
                clearcoatRoughness={0.15}
                roughness={0.9}
                metalness={0.8}
              />
            </mesh>
            <mesh geometry={nodes.clip.geometry} material={materials.metal} material-roughness={0.3} scale={1.5} position={[0, 1.5, 0]} rotation={[0, 0, Math.PI]} />
            <mesh geometry={nodes.clamp.geometry} material={materials.metal} scale={1.5} position={[0, 1.5, 0]} rotation={[0, 0, Math.PI]} />
          </group>
        </RigidBody>
      </group>
      <mesh ref={band}>
        <meshLineGeometry />
        <meshLineMaterial
          color="white"
          depthTest={false}
          resolution={isMobile ? [1000, 2000] : [1000, 1000]}
          useMap
          map={lanyardTexture}
          repeat={[-4, 1]}
          lineWidth={1.5}
        />
      </mesh>

      {/* Pocket Container - Decorative visual element */}
      <group position={isMobile ? [2, -2.8, -0.3] : [2, -3.2, -0.3]} scale={isMobile ? 0.85 : 1.0}>
        {/* Shadow/depth plane */}
        <mesh position={[0, 0, -0.2]}>
          <planeGeometry args={[3.2, 4.7]} />
          <meshStandardMaterial
            color="#000000"
            transparent
            opacity={0.15}
            roughness={1}
          />
        </mesh>

        {/* Back panel */}
        <mesh position={[0, 0, -0.1]}>
          <boxGeometry args={[3, 4.5, 0.1]} />
          <meshStandardMaterial
            color="#2a2420"
            roughness={0.85}
            metalness={0.08}
          />
        </mesh>

        {/* Left flap */}
        <mesh position={[-1.4, 0, 0.05]} rotation={[0, -0.25, 0]}>
          <boxGeometry args={[0.4, 4.5, 0.08]} />
          <meshStandardMaterial
            color="#252018"
            roughness={0.9}
            metalness={0.05}
          />
        </mesh>

        {/* Right flap */}
        <mesh position={[1.4, 0, 0.05]} rotation={[0, 0.25, 0]}>
          <boxGeometry args={[0.4, 4.5, 0.08]} />
          <meshStandardMaterial
            color="#252018"
            roughness={0.9}
            metalness={0.05}
          />
        </mesh>

        {/* Bottom curve */}
        <mesh position={[0, -2.2, 0]} rotation={[0, 0, Math.PI / 2]}>
          <cylinderGeometry args={[0.15, 0.15, 3, 16, 1, false, 0, Math.PI]} />
          <meshStandardMaterial
            color="#2a2420"
            roughness={0.85}
            metalness={0.08}
          />
        </mesh>

        {/* Top edge roll */}
        <mesh position={[0, 2.25, 0.05]} rotation={[0, 0, Math.PI / 2]}>
          <torusGeometry args={[0.08, 0.06, 8, 24, Math.PI]} />
          <meshStandardMaterial
            color="#3a3430"
            roughness={0.8}
            metalness={0.1}
          />
        </mesh>
      </group>
    </>
  );
}
